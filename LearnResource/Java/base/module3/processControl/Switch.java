class Switch {
    public static void main(String[] args) {
        int i = 3;
        char c = 4;
        switch (i) {
        case 3:
            System.out.println("the executive branch is case 3 branch");
            break;
        default:
            System.out.println("default branch");
            break;
        }
        switch (c) {
        case 4:
            System.out.println("the executive branch is case 4 branch");
            break;
        default:
            System.out.println("default branch");
            break;
        }
    }
}