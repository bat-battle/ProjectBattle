package com.form;
import com.tools.MySuperAction;

public class CarForm extends MySuperAction{
	public Integer id=null;
	public String username=null;
	public String user_number=null;
	public String car_number=null;
	public Integer tel=null;
	public String address=null;
	public String car_road=null;
	public String car_content=null;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	
	public String getUser_number() {
		return user_number;
	}
	public void setUser_number(String user_number) {
		this.user_number = user_number;
	}
	public String getCar_number() {
		return car_number;
	}
	public void setCar_number(String car_number) {
		this.car_number = car_number;
	}
	public Integer getTel() {
		return tel;
	}
	public void setTel(Integer tel) {
		this.tel = tel;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getCar_road() {
		return car_road;
	}
	public void setCar_road(String car_road) {
		this.car_road = car_road;
	}
	public String getCar_content() {
		return car_content;
	}
	public void setCar_content(String car_content) {
		this.car_content = car_content;
	}
	
	
}


